// runCLI 以内存 buffer 跑 esgw 命令，返回退出码与捕获的 stdout/stderr。
func runCLI(t *testing.T, args ...string) (int, string, string) {
	t.Helper()
	var stdout, stderr bytes.Buffer
	code := run(args, &stdout, &stderr)
	return code, stdout.String(), stderr.String()
}

func TestCompileStaticToFile(t *testing.T) {
	out := filepath.Join(t.TempDir(), "envoy.yaml")
	code, _, stderr := runCLI(t, "compile", "-f", "testdata/ok", "--mode", "static", "-o", out)
	if code != 0 {
		t.Fatalf("exit = %d, stderr:\n%s", code, stderr)
	}
	b, err := os.ReadFile(out)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.HasPrefix(string(b), "# Generated by esgw. DO NOT EDIT.\n# config_version: ") {
		t.Fatalf("missing header comments:\n%s", b)
	}
	if !strings.Contains(string(b), "esgw.config_version") {
		t.Fatalf("missing node.metadata version injection:\n%s", b)
	}
	if !strings.Contains(string(b), "lis/http") || !strings.Contains(string(b), "us/app") {
		t.Fatalf("missing resources:\n%s", b)
	}
}

func TestCompileXDS(t *testing.T) {
	out := filepath.Join(t.TempDir(), "snapshot.json")
	code, _, stderr := runCLI(t, "compile", "-f", "testdata/ok", "--mode", "xds", "-o", out)
	if code != 0 {
		t.Fatalf("exit = %d, stderr:\n%s", code, stderr)
	}
	b, err := os.ReadFile(out)
	if err != nil {
		t.Fatal(err)
	}
	var doc map[string]any
	if err := json.Unmarshal(b, &doc); err != nil {
		t.Fatalf("invalid JSON: %v\n%s", err, b)
	}
	for _, section := range []string{"version", "listeners", "clusters", "routes", "endpoints", "secrets"} {
		if _, ok := doc[section]; !ok {
			t.Fatalf("missing section %q:\n%s", section, b)
		}
	}
}

