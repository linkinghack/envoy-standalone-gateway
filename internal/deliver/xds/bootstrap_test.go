// validOpts 是一份合法的渲染参数（tcp admin 形态），各测试按需覆盖字段。
func validOpts() BootstrapOpts {
	return BootstrapOpts{
		NodeID:       "esgw-node",
		NodeCluster:  "esgw",
		XDSListen:    "127.0.0.1:18000",
		AdminAddress: "127.0.0.1:9901",
	}
}

// parseBootstrapYAML 把产物解析回 map 供断言（顺带验证产物是合法 YAML）。
func parseBootstrapYAML(t *testing.T, b []byte) map[string]any {
	t.Helper()
	var v map[string]any
	if err := yamlv3.Unmarshal(b, &v); err != nil {
		t.Fatalf("rendered output is not valid YAML: %v\n%s", err, b)
	}
	return v
}

// dig 按路径逐层下钻 map，任一层缺失即 fail。
func dig(t *testing.T, m map[string]any, path ...string) any {
	t.Helper()
	var cur any = m
	for _, p := range path {
		mm, ok := cur.(map[string]any)
		if !ok {
			t.Fatalf("path %v: %q is not a map", path, p)
		}
		cur, ok = mm[p]
		if !ok {
			t.Fatalf("path %v: missing key %q", path, p)
		}
	}
	return cur
}

func TestRenderBootstrapHeader(t *testing.T) {
	b, err := RenderBootstrap(validOpts())
	if err != nil {
		t.Fatal(err)
	}
	// 文件头与 static.Render 同款首行；写 node_id 而非 config_version
	// （接入 bootstrap 与配置版本无关，见 T4 进展记录）。
	if !strings.HasPrefix(string(b), "# Generated by esgw. DO NOT EDIT.\n# node_id: esgw-node\n") {
		t.Fatalf("missing header comments:\n%s", b)
	}
	if strings.Contains(string(b), "config_version") {
		t.Fatalf("bootstrap must not carry config_version:\n%s", b)
	}
}

func TestRenderBootstrapDefaults(t *testing.T) {
	opts := validOpts()
	opts.NodeID = ""
	opts.NodeCluster = ""
	b, err := RenderBootstrap(opts)
	if err != nil {
		t.Fatal(err)
	}
	doc := parseBootstrapYAML(t, b)
	if got := dig(t, doc, "node", "id"); got != "esgw-node" {
		t.Fatalf("node.id = %v, want esgw-node", got)
	}
	if got := dig(t, doc, "node", "cluster"); got != "esgw" {
		t.Fatalf("node.cluster = %v, want esgw", got)
	}
}

