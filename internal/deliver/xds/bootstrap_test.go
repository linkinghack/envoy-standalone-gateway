func TestRenderBootstrapInvalidAdminAddress(t *testing.T) {
	for _, addr := range []string{
		"",                     // 空
		"unix://",              // scheme 后无路径
		"tcp://127.0.0.1:9901", // 不支持的 scheme
		"127.0.0.1",            // 缺端口
		"127.0.0.1:0",          // 端口越界
		"127.0.0.1:70000",      // 端口越界
		"127.0.0.1:abc",        // 端口非数字
	} {
		opts := validOpts()
		opts.AdminAddress = addr
		if _, err := RenderBootstrap(opts); err == nil {
			t.Fatalf("adminAddress %q: want error", addr)
		}
	}
}

