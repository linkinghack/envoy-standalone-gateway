func TestRenderBootstrapKeyFields(t *testing.T) {
	b, err := RenderBootstrap(validOpts())
	if err != nil {
		t.Fatal(err)
	}
	doc := parseBootstrapYAML(t, b)

	// dynamic_resources：ads_config GRPC/V3 指向 esgw_xds；cds/lds 走 ads。
	ads := dig(t, doc, "dynamic_resources", "ads_config").(map[string]any)
	if ads["api_type"] != "GRPC" || ads["transport_api_version"] != "V3" {
		t.Fatalf("ads_config = %v:\n%s", ads, b)
	}
	svcs := ads["grpc_services"].([]any)
	if len(svcs) != 1 {
		t.Fatalf("grpc_services = %v", svcs)
	}
	if got := svcs[0].(map[string]any)["envoy_grpc"].(map[string]any)["cluster_name"]; got != XDSClusterName {
		t.Fatalf("ads grpc cluster_name = %v, want %q", got, XDSClusterName)
	}
	for _, k := range []string{"cds_config", "lds_config"} {
		cs := dig(t, doc, "dynamic_resources", k).(map[string]any)
		if cs["resource_api_version"] != "V3" {
			t.Fatalf("%s = %v", k, cs)
		}
		if _, ok := cs["ads"]; !ok {
			t.Fatalf("%s missing ads: %v", k, cs)
		}
	}

	// static_resources.clusters[esgw_xds]：STATIC、显式 h2、load_assignment
	// 指向 XDSListen。
	clusters := dig(t, doc, "static_resources", "clusters").([]any)
	if len(clusters) != 1 {
		t.Fatalf("clusters = %v", clusters)
	}
	cl := clusters[0].(map[string]any)
	if cl["name"] != XDSClusterName || cl["type"] != "STATIC" {
		t.Fatalf("xds cluster = %v", cl)
	}
	if cl["connect_timeout"] != "5s" {
		t.Fatalf("xds cluster connect_timeout = %v", cl["connect_timeout"])
	}
	// lb_policy: ROUND_ROBIN 是枚举零值，protojson 省略（产物风格与
	// static.Render 一致：零值字段不输出）；缺省即 Envoy 默认 ROUND_ROBIN。
	if v, ok := cl["lb_policy"]; ok && v != "ROUND_ROBIN" {
		t.Fatalf("xds cluster lb_policy = %v, want ROUND_ROBIN (or omitted)", v)
	}
	hpo := cl["typed_extension_protocol_options"].(map[string]any)["envoy.extensions.upstreams.http.v3.HttpProtocolOptions"].(map[string]any)
	if _, ok := hpo["explicit_http_config"].(map[string]any)["http2_protocol_options"]; !ok {
		t.Fatalf("HttpProtocolOptions missing explicit h2: %v", hpo)
	}
	ep := dig(t, cl, "load_assignment", "endpoints").([]any)[0].(map[string]any)["lb_endpoints"].([]any)[0]
	sa := dig(t, ep.(map[string]any), "endpoint", "address", "socket_address").(map[string]any)
	if sa["address"] != "127.0.0.1" || sa["port_value"] != 18000 {
		t.Fatalf("esgw_xds endpoint = %v, want 127.0.0.1:18000", sa)
	}
}

// TestRenderBootstrapEnvoyValidate 用真实 envoy 二进制做 --mode validate
// （A7 单测层证据）。复用 envoycheck 的发现顺序；无二进制时按既有约定
// 降级 skip（同 compile.go runEnvoyCheck 的 Warning 降级语义）。
func TestRenderBootstrapEnvoyValidate(t *testing.T) {
	bin, err := envoycheck.FindBinary("")
	if err != nil {
		if errors.Is(err, envoycheck.ErrBinaryNotFound) {
			t.Skipf("envoy binary not found, skip --mode validate: %v", err)
		}
		t.Fatal(err)
	}
	b, err := RenderBootstrap(validOpts())
	if err != nil {
		t.Fatal(err)
	}
	if err := envoycheck.Validate(context.Background(), bin, b, 0); err != nil {
		t.Fatalf("envoy --mode validate rejected bootstrap:\n%v\n%s", err, b)
	}
}
